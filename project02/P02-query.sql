-- MARK: Query
SELECT
	rd.rider_name AS name,
	(t.total_time + t.total_penalty - t.total_bonus) AS time
FROM (
	SELECT
		r.rider_bib,
		SUM(r.time) AS total_time,
		SUM(r.penalty) AS total_penalty,
		SUM(
			CASE
				WHEN r.rank = 1 THEN 10
	        	WHEN r.rank = 2 THEN 6
	        	WHEN r.rank = 3 THEN 4
				ELSE 0
			END
		) AS total_bonus
	FROM result r
	WHERE r.rider_bib NOT IN (
	    SELECT rider_bib FROM exit
	)
	GROUP BY r.rider_bib
) AS t, rider rd
WHERE t.rider_bib = rd.rider_bib
ORDER BY time ASC
LIMIT 1;